/**
 * يُستدعى من بناء Netlify: يكتب adora-config.js من:
 * 1) متغير البيئة ADORA_API_BASE (من لوحة Netlify)، أو
 * 2) أول سطر https:// في render-api-url.txt في جذر المشروع.
 */
const fs = require("fs");
const path = require("path");

function readUrlFromRenderFile() {
  const p = path.join(__dirname, "..", "render-api-url.txt");
  if (!fs.existsSync(p)) return "";
  const raw = fs.readFileSync(p, "utf8");
  for (const line of raw.split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    if (t.startsWith("http://") || t.startsWith("https://")) {
      if (t.includes("YOUR-SERVICE") || t.includes("REPLACE_ME")) return "";
      return t.replace(/\/$/, "");
    }
  }
  return "";
}

const out = path.join(__dirname, "..", "adora-config.js");
let url = String(process.env.ADORA_API_BASE || "")
  .trim()
  .replace(/\/$/, "");
if (!url) url = readUrlFromRenderFile();
const content = url
  ? `/* Generated by Netlify build — ADORA_API_BASE */
window.ADORA_API_BASE = ${JSON.stringify(url)};
window.ADORA_API_ORIGIN = ${JSON.stringify(url)};
`
  : `/* فارغ = نفس أصل الصفحة (تطوير محلي). للنشر: اضبط ADORA_API_BASE في Netlify أو عدّل هذا الملف. */
window.ADORA_API_BASE = "";
window.ADORA_API_ORIGIN = "";
`;
fs.writeFileSync(out, content, "utf8");
if (url) {
  // eslint-disable-next-line no-console
  console.log("[adora] ADORA_API_BASE ->", url);
} else {
  // eslint-disable-next-line no-console
  console.log("[adora] ADORA_API_BASE empty (set in Netlify env or edit adora-config.js)");
}
